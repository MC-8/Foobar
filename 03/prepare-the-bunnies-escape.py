from collections import namedtuple, deque
from itertools import product
from copy import deepcopy

def get_path_length(maze):
    
    state = namedtuple('state', 'position steps')
    path_length = float('inf')
    have_been = {} # Key: position, value: steps
    initial_state = state((0,0),1)
    xmax, ymax = len(maze)-1, len(maze[0])-1
    final_position = (xmax, ymax)

    Q = deque()
    Q.append(initial_state)
    have_been[initial_state.position] = initial_state.steps
    
    # Base exploration.
    while Q:
        s = Q.popleft()
        if s.position==final_position:
            path_length = min(path_length,s.steps)
            continue
        for dx,dy in ((0,1),(1,0),(-1,0),(0,-1)):
            newx = s.position[0]+dx
            newy = s.position[1]+dy
            if 0<=newx<=xmax and 0<=newy<=ymax and maze[newx][newy]==0:
                newstate = state((newx, newy), s.steps+1)
                if newstate.position in have_been and have_been[newstate.position] <= newstate.steps:
                    continue
                Q.append(newstate)
                have_been[newstate.position] = newstate.steps

    return path_length

def solution(maze):
    min_length = float('inf')
    
    xmax, ymax = len(maze)-1, len(maze[0])-1
    for x,y in product(range(xmax+1), range(ymax+1)):
        newmaze = deepcopy(maze)
        newmaze[x][y] = 0
        min_length = min(min_length, get_path_length(newmaze))

    return min_length


